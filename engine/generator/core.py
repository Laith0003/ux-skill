def _strip_dashes(text: str) -> str:
    """No em-dashes or en-dashes (house rule + keeps the YAML scalar clean)."""
    return (text or "").replace("—", "-").replace("–", "-").replace("‒", "-")


def _emit_design_md(rec: Recommendation, brief: Optional[Brief] = None) -> str:
    palette = rec.palette or {}
    colors = palette.get("colors", {})
    tp = rec.type_pair or {}
    style = rec.style or {}

    display = (tp.get("display") or {}).get("family") or "Georgia"
    body = (tp.get("body") or {}).get("family") or "system-ui"
    mono = (tp.get("mono") or {}).get("family") or "ui-monospace"
    pal_name = palette.get("name") or palette.get("id") or "custom palette"
    style_name = style.get("name") or style.get("id") or "custom"

    project = "ux-skill"
    if brief is not None:
        brand = getattr(brief, "brand", None)
        if isinstance(brand, dict) and brand.get("name"):
            project = str(brand["name"])
        elif getattr(brief, "project_type", None):
            project = str(brief.project_type)
    project = (_strip_dashes(project).strip() or "ux-skill").replace(" ", "-").lower()

    description = _strip_dashes(
        f"{style_name} system on the {pal_name} palette. "
        f"Display type {display}, body {body}, mono {mono}. "
        f"Generated by ux-skill: deterministic, offline, never calls an LLM."
    ).replace('"', "'")

    out: List[str] = ["---", "version: alpha", f"name: {project}-design-analysis",
                      f'description: "{description}"', "colors:"]
    for key, value in colors.items():
        if isinstance(value, str):
            dk = _DESIGN_MD_COLOR_RENAME.get(key, key).replace("_", "-")
            out.append(f'  {dk}: "{value}"')
    out.append('  on-primary: "#ffffff"')

    out.append("typography:")
    for tok, fam, sz, wt, lh, ls in [
        ("display-xl", display, 64, 600, "1.05", "-1.5"),
        ("display-lg", display, 48, 600, "1.1", "-1"),
        ("display-md", display, 36, 600, "1.15", "-0.5"),
        ("display-sm", display, 28, 600, "1.2", "-0.3"),
        ("body", body, 16, 400, "1.6", "0"),
        ("body-sm", body, 14, 400, "1.55", "0"),
        ("button", body, 15, 500, "1", "0.1"),
        ("caption", body, 12, 400, "1.4", "0.2"),
        ("code", mono, 13, 400, "1.5", "0"),
    ]:
        out.append(f"  {tok}:")
        out += [f'    fontFamily: "{fam}"', f"    fontSize: {sz}px",
                f"    fontWeight: {wt}", f"    lineHeight: {lh}", f"    letterSpacing: {ls}px"]

    out.append("spacing:")
    out += [f"  {k}: {v}px" for k, v in
            [("xxs", 4), ("xs", 8), ("sm", 12), ("md", 16), ("lg", 24), ("xl", 32), ("xxl", 48), ("xxxl", 64)]]

    out.append("rounded:")
    out += [f"  {k}: {v}" for k, v in
            [("xs", "4px"), ("sm", "6px"), ("md", "8px"), ("lg", "12px"), ("xl", "16px"), ("pill", "9999px"), ("full", "9999px")]]

    out.append("components:")
    out.append(_DESIGN_MD_COMPONENTS)
    out.append("---")
    return "\n".join(out) + "\n"


def design_md(rec: Recommendation, brief: Optional[Brief] = None,
              out_path: str = "./DESIGN.md") -> Dict[str, Any]:
    """Write a DESIGN.md from a Recommendation. Returns a small summary dict."""
    target = Path(out_path)
    if str(target.parent) not in ("", "."):
        target.parent.mkdir(parents=True, exist_ok=True)
    content = _emit_design_md(rec, brief)
    target.write_text(content, encoding="utf-8")
    palette = rec.palette or {}
    return {
        "file_written": str(target),
        "format": "DESIGN.md (Google Stitch / awesome-design-md standard)",
        "bytes": len(content),
        "picks": {
            "palette": palette.get("id"),
            "type_pair": (rec.type_pair or {}).get("id"),
            "colors": len([v for v in palette.get("colors", {}).values() if isinstance(v, str)]) + 1,
        },
    }
