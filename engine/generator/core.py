# A single markdown file an AI agent reads to generate on-brand UI. ux-skill
# generates a NOVEL one from a Recommendation (vs copying a frozen brand file),
# mapping the synthesized palette + type pair onto the 8-key schema. Pure
# string building: deterministic, offline, no third-party YAML dependency.
# ---------------------------------------------------------------------------

