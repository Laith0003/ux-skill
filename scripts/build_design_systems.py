def main() -> None:
    if OUT.exists():
        shutil.rmtree(OUT)
    OUT.mkdir(parents=True, exist_ok=True)
    index = []
    for slug, name, brief, description, concept, tags in CATALOG:
        system = synthesize(brief)
        res = pack_system(system, name=name, description=description, concept=concept,
                          out_dir=str(OUT), slug=slug, tags=tags)
        zip_path = OUT / f"{slug}.zip"
        _zip_folder(OUT / slug, zip_path)
        pal = res["palette"]
        index.append({
            "slug": slug, "name": name, "theme": res["theme"], "description": description,
            "display_font": system.type_pair.get("display", "Inter"),
            "body_font": system.type_pair.get("body", "Inter"),
            "tags": tags, "palette": pal,
            "swatches": [pal[k] for k in ("canvas", "surface", "ink", "body", "primary",
                                          "success", "warning", "danger", "info") if k in pal],
            "primary": pal["primary"], "canvas": pal["canvas"], "ink": pal["ink"],
            "contrast": res["ratios"], "wcag_aa": res["wcag_aa_text"],
            "zip": f"{slug}.zip", "files": ["DESIGN.md", "metadata.json", "css/tokens.css", "preview.html"],
        })
        print(f"  {name:10} {res['theme']:5} canvas {pal['canvas']} primary {pal['primary']} "
              f"AA={res['wcag_aa_text']} zip={zip_path.stat().st_size}B")
    (OUT / "index.json").write_text(json.dumps({"systems": index, "count": len(index)},
                                               indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(f"\nWrote {len(index)} systems + index.json to {OUT}/")
    size = build_gallery(index)
    print(f"Wrote docs/design-systems.html ({size} bytes)")


if __name__ == "__main__":
    main()
